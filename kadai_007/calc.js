// 変数 num に任意の値を代入
let num = 15; 

// 条件1: 3と5の倍数かどうかをチェック
if (num % 3 === 0 && num % 5 === 0) {
    console.log("3と5の倍数です");

// 条件2: 3の倍数かどうかをチェック（条件1が成立しなかった場合）
} else if (num % 3 === 0) {
    console.log("3の倍数です");

// 条件3: 5の倍数かどうかをチェック（条件1と2が成立しなかった場合）
} else if (num % 5 === 0) {
    console.log("5の倍数です");

// それ以外の場合
} else {
    console.log(num);
}
